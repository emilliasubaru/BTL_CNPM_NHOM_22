    private void fillCbb() {
        Vector<TheLoaiDongGopModel> vcbb =  tldao.findTL((ArrayList<TheLoaiDongGopModel>) tldao.findAll());
        vcbb.remove(0);
        //Vector vcbb = tldao.findAll();
        if (vcbb != null) {
            DefaultComboBoxModel cbbmodel = new DefaultComboBoxModel(vcbb);
            cbbMaTL.setModel(cbbmodel);
            for(int i =0; i<cbbMaTL.getItemCount();i++){
                TheLoaiDongGopModel tl = ((TheLoaiDongGopModel)cbbMaTL.getItemAt(i));
                if(dg.getMaTheLoaiDG()== tl.getMaTheLoaiDG()){
                    cbbMaTL.setSelectedIndex(i);
                }
            }
        } else {
        }
    }
