    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!validate(txtMaDG,txtMaDG.getText(),"Mã Đóng Góp")){
            return;
        }
        if(!validate(txtMaHK,txtMaHK.getText(),"Mã Hộ Khẩu")){
            return;
        }
        if(!validate(txtTien,txtTien.getText(),"Số Tiền")){
            return;
        }
        if(!validate(jDateChooser1,jDateChooser1.getDateFormatString(),"Ngày Thu")){
            return;
        }
        DongGopModel dg = new DongGopModel();
        dg.setMaDongGop(Integer.parseInt(txtMaDG.getText()));
        dg.setMaHoKhau(Integer.parseInt(txtMaHK.getText()));
        int maTheLoai = ((TheLoaiDongGopModel)cbbMaTL.getSelectedItem()).getMaTheLoaiDG();
        dg.setMaTheLoaiDG(maTheLoai);
        dg.setSoTienDG(Integer.parseInt(txtTien.getText()));
        dg.setNgayDG(jDateChooser1.getDate());
        dg.setGhiChu(txtGhiChu.getText());
        dgdao.update(dg);
        jpQLDongGop.qldg.fillTbl(maTheLoai);
        jpQLDongGop.qldg.updateUI();
        this.dispose(); 
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

