    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int select=tblDG.getSelectedRow();
        if(select<0){
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn hàng nào !");
        }else{
            int maDG =  (int) tblDG.getValueAt(select, 0);
            dlSuaDG sua = new dlSuaDG(Run.QLDongGop, true, maDG);
            sua.setVisible(true);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int selectedIndex = tblDG.getSelectedRow();
        if(selectedIndex < 0) JOptionPane.showMessageDialog(null, "Bạn chưa chọn hàng nào !");
        else {
            DongGopModel dg = dgList.get(selectedIndex);
            int option = JOptionPane.showConfirmDialog(this, "Do you want to delete this?");

            if (option == 0) {
                dgdao.delete(dg.getMaDongGop());
                fillTbl(dg.getMaTheLoaiDG());
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnXoaActionPerformed


