    NumberFormat chuyentien = new DecimalFormat("#,###,###");
    SimpleDateFormat sf = new SimpleDateFormat("dd/MM/yyyy");
    public void fillTbl(int maTL) {
        if(maTL == 0) dgList = dgdao.findAll();
        else dgList = dgdao.findByTheLoai(maTL);
        tableModel.setRowCount(0);
        dgList.forEach((dg) -> {
            tableModel.addRow(new Object[]{dg.getMaDongGop(), dg.getMaHoKhau(), chuyentien.format(dg.getSoTienDG()), dg.getMaTheLoaiDG(), sf.format(dg.getNgayDG()), dg.getGhiChu()});
        });
    }
